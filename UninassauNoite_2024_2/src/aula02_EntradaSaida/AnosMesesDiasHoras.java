package aula02_EntradaSaida;

public class AnosMesesDiasHoras {
	
	public static void main(String[] args) {
		
		int idade = 34;
		int meses = idade * 12;
		int dias = idade * 365;
		int minutos = dias * 24 * 60;
		System.out.println("Você viveu "+meses+ " meses");
		System.out.println("Você viveu "+dias+ " dias");
		System.out.println("Você viveu "+minutos+ " minutos");
		
		
	}

}
